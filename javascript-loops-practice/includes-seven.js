function includesSeven(array) {
  for (let i = 0; i < array.length; i++) {
    var currentValue = array[i];
    var results;
    if (currentValue === 7) {
      return true;
      results = 'true';
      }
    }
    if (results != 'true') {
      return false;
    }
  }
